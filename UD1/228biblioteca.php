    /*  crea un archivo con funciones para sumar, restar, multiplicar y dividir dos números.*/
    function sumar($a, $b){
        return $a + $b;
    }
    function multiplicar($a, $b){
        return $a * $b;
    }
    function res($a, $b){
        return $a - $b;
    }
    function div($a, $b){
        return $a / $b;
    }

