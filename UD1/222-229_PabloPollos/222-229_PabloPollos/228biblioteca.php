<?php
    /*crea un archivo con funciones para sumar, restar, multiplicar y dividir dos números.*/
    function sumar($num1, $num2){
        return $num1+$num2;
    }
    function restar($num1, $num2){
        return $num1-$num2;
    }
    function multiplicar($num1, $num2){
        return $num1*$num2;
    }
    function dividir($num1, $num2){
        return $num1/$num2;
    }

   

?>